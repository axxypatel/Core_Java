    private void searchPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchPatientActionPerformed
        // TODO add your handling code here:

        String patientName = nameTextField.getText();
        String patientMRN = numberTextField.getText();
        DefaultTableModel dtm = (DefaultTableModel) patientJTable.getModel();
        dtm.setRowCount(0);
        //PatientList patientList = enterprise.getPatientListObject();
        //PatientList patientList = system.getPatientList();
        if ("".equals(patientName) && "".equals(patientMRN)) {
            JOptionPane.showMessageDialog(null, "Please enter the Patient Name or Patient MRN.");
        } else {
            for (Patient patient : system.getPatientList().getPatientList()) {
                String patientObject = patient.getPatientName();
                if (!"".equals(patientName)) {
                    if (patientObject.toLowerCase().contains(patientName.toLowerCase())) {
                        Object[] row = new Object[dtm.getColumnCount()];
                        row[0] = patient;
                        row[1] = patient.getPatientName();
                        row[2] = patient.getPatientSex();
                        row[3] = patient.getAge();
                        dtm.addRow(row);
                    }
                } else if (!"".equals(patientMRN)) {
                    if (String.valueOf(patient.getMRN()).contains(patientMRN)) {
                        Object[] row = new Object[dtm.getColumnCount()];
                        row[0] = patient;
                        row[1] = patient.getPatientName();
                        row[2] = patient.getPatientSex();
                        row[3] = patient.getAge();
                        dtm.addRow(row);
                    }
                }
            }
        }
    }//GEN-LAST:event_searchPatientActionPerformed

