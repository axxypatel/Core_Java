    public void populateDrug() {
        DefaultTableModel dtm = (DefaultTableModel) ProductSearchJTable.getModel();
        dtm.setRowCount(0);
        String searchtext = txtSearchDrug.getText().trim().toLowerCase();

        for (Drug d : system.getDrugListObject().getDrugList()) {
            if (d.getDrugName().trim().toLowerCase().contains(searchtext)) {
                Object[] row = new Object[dtm.getColumnCount()];
                row[0] = d;
                row[1] = d.getStrength() + " " + d.getStrengthUnit();
                row[2] = d.getVolume() + " " + d.getVolumeUnit();
                row[3] = d.getDispenseQty() + " " + d.getDispenseQtyUnit();
                dtm.addRow(row);
            }
        }
    }
