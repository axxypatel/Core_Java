    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        int selectedRow;
        Drug drug = null;
        Lab lab = null;
        if (rbDrug.isSelected()) {
            selectedRow = ProductSearchJTable.getSelectedRow();
            if (selectedRow >= 0) {
                drug = (Drug) ProductSearchJTable.getValueAt(selectedRow, 0);
            } else {
                JOptionPane.showMessageDialog(null, "Please select drug.");
                return;
            }
        } else {
            selectedRow = LabSearchJTable.getSelectedRow();
            if (selectedRow >= 0) {
                lab = (Lab) LabSearchJTable.getValueAt(selectedRow, 0);
            } else {
                JOptionPane.showMessageDialog(null, "Please select lab.");
                return;
            }
        }
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("AddEditHighRiskCategoriesJPanel", new AddHighRiskCategoriesJPanel(userProcessContainer, doctorOrganization, enterprise, drug, lab));
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnNextActionPerformed

